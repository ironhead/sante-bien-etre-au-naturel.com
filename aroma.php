<h1>Qu'est ce que c'est en bref ?</h1>

<p>L'aromathérapie consiste en l'utilisation des huiles essentielles chémotypées ou HECT pour se soigner. Elles sont utilisées par voie externe ou interne.
Les HECT de plantes aromatiques sont des produits à très forte concentration en principes actifs bien diversifiés ; elles n'ont pas tout à fait les mêmes indications que les plantes entières dont elles sont issues. Elles doivent être appliquées avec précaution sans jamais dépasser la dose prescrite.</p>

<p>La majorité des huiles essentielles sont obtenues par distillation par entraînement à la vapeur d'eau sous-basse pression. Le procédé consiste à faire traverser par de la vapeur d'eau une cuve remplie de plantes aromatiques.</p>

<p>Les HECT doivent cependant  respecter des critères de qualité validant leur utilisation thérapeutique. En effet, les Huiles Essentielles utilisées en cosmétique ne sont pas compatibles à un usage thérapeutique.</p>

<p>En effet, les huiles essentielles doivent être 100% pures et naturelles, l'appellation AB (agriculture biologique) est un plus.</p>

<p>Les huiles essentielles ont de nombreuses propriétés qui font d'elles un accompagnement santé et bien-être idéales. Pour ne citer que quelques une de ses propriétés :</p>
<ul>
	<li>anti-inflammatoire</li>
	<li>antalgique</li>
	<li>anti-infectieuse</li>
	<li>anti-bactérienne</li>
	<li>anti-fongique</li>
	<li>tonique général</li>
	<li>décongestionnante ...</li>
</ul>
Et bien d'autres encore.

